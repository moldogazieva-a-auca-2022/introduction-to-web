print("Using for loop:")
for i in range(1, 11):
    print(i)

print("\nUsing while loop:")
i = 1
while i <= 10:
    print(i)
    i += 1

print("\nIterating over 'Python' with a for loop:")
for char in "Python":
    print(char)

print("\nUsing while loop to print even numbers from 2 to 10:")
num = 2
while num <= 10:
    print(num)
    num += 2
